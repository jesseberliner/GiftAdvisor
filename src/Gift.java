public class Gift
{
    private String giftName;

    private String giftGender;

    private String giftPrice;

    private String giftAge;

    //Constructors


    //Getters and Setters
    public String getGiftName()
    {
        return giftName;
    }

    public void setGiftName(String giftName)
    {
        this.giftName = giftName;
    }

    public String getGiftGender()
    {
        return giftGender;
    }

    public void setGiftGender(String giftGender)
    {
        this.giftGender = giftGender;
    }

    public String getGiftPrice()
    {
        return giftPrice;
    }

    public void setGiftPrice(String giftPrice)
    {
        this.giftPrice = giftPrice;
    }

    public String getGiftAge()
    {
        return giftAge;
    }

    public void setGiftAge(String giftAge)
    {
        this.giftAge = giftAge;
    }
}
